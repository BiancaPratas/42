void	ft_rev_int_tab(int *tab, int size)
{
	int x;
	int troc;

	x = 0;
	while (x < size)
	{
		troc = tab[x];
		tab[x] = tab[size - 1];
		tab[size - 1] = troc;
		x++;
		size--;
	}
}
